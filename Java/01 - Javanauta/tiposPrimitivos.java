public class tiposPrimitivos {
    public static void main(String[] args) {
        // Tipos primitivos em Java:
        // 1. Números inteiros (byte, short, int, long)
        // 2. Números de ponto flutuante (float, double)
        // 3. Caracteres (char)
        // 4. Booleanos (boolean)
        int idade = 25;
        double salarioMensal = 3500.50;
        char genero = 'F';
        boolean empregadoJava = true;

        System.out.println("Sua idade é: " + idade);
        System.out.println("Seu salário mensal é: " + salarioMensal);
        System.out.println("Seu gênero é: " + genero);
        System.out.println("Empregado em Java: " + empregadoJava);
    }
}